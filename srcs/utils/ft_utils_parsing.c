#include "minishell.h"

char	*ft_sep(t_arg *arg, int i, char c, int count)
{
	int	z;

	z = 0;
	while (z < count)
	{
		arg->cmds[i] = &c;
		z++;
	}
	return (arg->cmds[i]);
}
